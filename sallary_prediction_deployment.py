import streamlit as st
import pandas as pd
import joblib

model=joblib.load("SalaryData (1).csv")
encoder=joblib.load("label_encoder.pkl")

st.title("salary prediction app")
age=st.number_input("Age",18,60)
gender=st.selectbox("Gender",encoder["Gender"].classes_)
education=st.selectbox("Education",encoder["Education Level"].classes_)
job_title=st.selectbox("Job Title",encoder["Job Title"].classes_)
years_of_experience=st.number_input("Years of Experience",0,20)

df=pd.DataFrame({
    "Age":[age],
    "Gender":[gender],
    "Education Level":[education],
    "Job Title":[job_title],
    "Years of Experience":[years_of_experience]


})
if st.button("Predict"):
  for col in df.columns:
    df[col]=encoder[col].transform(df[col])
    prediction=model.predict(df)
    st.success(f"The predicted salary is {prediction[0]:,2f}")

























